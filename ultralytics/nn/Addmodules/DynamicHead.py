class ChannelAttention(nn.Module):
    """通道注意力模块"""
    def __init__(self, in_planes, reduction=16):
        super(ChannelAttention, self).__init__()
        self.avg_pool = nn.AdaptiveAvgPool2d(1)  # 全局平均池化
        self.max_pool = nn.AdaptiveMaxPool2d(1)  # 全局最大池化

        # 使用共享的全连接层，减少通道数
        self.fc1 = nn.Conv2d(in_planes, in_planes // reduction, 1, bias=False)
        self.relu = nn.ReLU()
        self.fc2 = nn.Conv2d(in_planes // reduction, in_planes, 1, bias=False)
        self.sigmoid = nn.Sigmoid()

    def forward(self, x):
        avg_out = self.fc2(self.relu(self.fc1(self.avg_pool(x))))  # 平均池化路径
        max_out = self.fc2(self.relu(self.fc1(self.max_pool(x))))  # 最大池化路径
        out = avg_out + max_out
        return self.sigmoid(out) * x


class SpatialAttention(nn.Module):
    """空间注意力模块"""
    def __init__(self, kernel_size=7):
        super(SpatialAttention, self).__init__()
        self.conv = nn.Conv2d(2, 1, kernel_size, padding=(kernel_size - 1) // 2, bias=False)
        self.sigmoid = nn.Sigmoid()

    def forward(self, x):
        avg_out = torch.mean(x, dim=1, keepdim=True)  # 按通道求平均值
        max_out, _ = torch.max(x, dim=1, keepdim=True)  # 按通道取最大值
        out = torch.cat([avg_out, max_out], dim=1)  # 拼接两种特征
        out = self.conv(out)
        return self.sigmoid(out) * x


class CBAM(nn.Module):
    """CBAM: 通道注意力 + 空间注意力"""
    def __init__(self, in_planes, reduction=16, kernel_size=7):
        super(CBAM, self).__init__()
        self.channel_attention = ChannelAttention(in_planes, reduction=reduction)
        self.spatial_attention = SpatialAttention(kernel_size=kernel_size)

    def forward(self, x):
        x = self.channel_attention(x)
        x = self.spatial_attention(x)
        return x

class Detect_advanced(nn.Module):
    """Enhanced YOLO Detection Head with CBAM."""
    dynamic = False  # force grid reconstruction
    export = False  # export mode
    shape = None
    anchors = torch.empty(0)  # init
    strides = torch.empty(0)  # init

    def __init__(self, nc=80, ch=(), reg_max=16):
        super().__init__()
        self.nc = nc
        self.nl = len(ch)
        self.reg_max = reg_max
        self.no = nc + self.reg_max * 4
        self.stride = torch.zeros(self.nl)

        c2 = max(16, ch[0] // 4, self.reg_max * 4)
        c3 = max(ch[0], min(self.nc, 100))

        self.reg_heads = nn.ModuleList(
            nn.Sequential(
                Conv(x, c2, 3), Conv(c2, c2, 3), nn.Conv2d(c2, 4 * self.reg_max, 1)
            ) for x in ch
        )
        self.cls_heads = nn.ModuleList(
            nn.Sequential(
                Conv(x, c3, 3), Conv(c3, c3, 3), nn.Conv2d(c3, self.nc, 1)
            ) for x in ch
        )

        # Attention mechanism: CBAM
        self.attention_layers = nn.ModuleList(
            CBAM(ch[i]) for i in range(self.nl)
        )

        # Lightweight feature refinement
        self.refinement_layers = nn.ModuleList(
            nn.Sequential(
                nn.Conv2d(ch[i], ch[i], kernel_size=3, stride=1, padding=1, groups=ch[i]),
                nn.BatchNorm2d(ch[i]),
                nn.ReLU(inplace=True)
            ) for i in range(self.nl)
        )

    def forward(self, x):
        for i in range(self.nl):
            x[i] = self.refinement_layers[i](self.attention_layers[i](x[i]))

        regression_preds, classification_preds = [], []

        for i in range(self.nl):
            reg_out = self.reg_heads[i](x[i])  # [batch_size, 4 * reg_max, H, W]
            cls_out = self.cls_heads[i](x[i])  # [batch_size, nc, H, W]

            # Flatten and store predictions
            reg_out = reg_out.flatten(2)  # [batch_size, 4 * reg_max, H*W]
            cls_out = cls_out.flatten(2)  # [batch_size, nc, H*W]

            regression_preds.append(reg_out)
            classification_preds.append(cls_out)

        # Concatenate predictions along spatial dimension
        regression_preds = torch.cat(regression_preds, dim=2)  # [batch_size, 4 * reg_max, total_spatial]
        classification_preds = torch.cat(classification_preds, dim=2)  # [batch_size, nc, total_spatial]

        if self.training:
            return regression_preds, classification_preds
        else:
            # Decode boxes during inference
            regression_preds = dist2bbox(regression_preds, self.anchors, self.strides)
            classification_preds = torch.sigmoid(classification_preds)
            return regression_preds, classification_preds

    def bias_init(self):
        """Initialize biases for classification and regression heads."""
        for reg_head, cls_head in zip(self.reg_heads, self.cls_heads):
            # Initialize regression biases
            reg_head[-1].bias.data.fill_(1.0)

            # Initialize classification biases
            cls_head[-1].bias.data.fill_(-math.log((1 - 0.01) / 0.01))  # log(0.01/0.99)
